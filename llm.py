def _is_critical(messages: list) -> bool:
    """True если последний ответ TENGU содержал отказ — эскалируем на Opus."""
    ai_msgs = [m['content'] for m in messages if m['role'] == 'assistant']
    if not ai_msgs:
        return False
    last = ai_msgs[-1].lower()
    return any(p in last for p in _REFUSAL_SIGNALS)

